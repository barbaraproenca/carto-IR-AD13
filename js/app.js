class AD13Cartography {
  constructor() {
    this.data = AD13_DATA;
    this.currentFilter = 'all';
    this.currentTheme = null;
    this.searchTerm = '';
    this.allInstruments = [];
    
    this.init();
  }

  init() {
    this.buildHierarchy();
    this.buildSeriesButtons();
    this.buildThemeList();
    this.collectAllInstruments();
    this.createSunburst();
    this.setupEventListeners();
    this.updateResults();
  }

  buildHierarchy() {
    const hierarchy = {
      name: "AD13",
      children: []
    };

    const series = this.data.cadre_classement.series;
    
    Object.keys(series).forEach(seriesKey => {
      const seriesData = series[seriesKey];
      const seriesNode = {
        name: seriesKey,
        fullName: seriesData.intitule,
        description: seriesData.description,
        periode: seriesData.periode,
        color: seriesData.color || '#718096',
        children: []
      };

      if (seriesData.instruments && seriesData.instruments.length > 0) {
        seriesData.instruments.forEach(inst => {
          seriesNode.children.push({
            name: inst.cote,
            fullName: inst.description,
            description: inst.description,
            periode: inst.periode || seriesData.periode,
            producer: inst.producteur,
            color: seriesData.color
          });
        });
      }

      if (seriesKey === 'W' && seriesData.thematiques) {
        Object.keys(seriesData.thematiques).forEach(themeKey => {
          const theme = seriesData.thematiques[themeKey];
          const themeNode = {
            name: themeKey,
            fullName: theme.intitule,
            description: theme.description,
            color: this.adjustColor(seriesData.color, 20),
            children: []
          };

          theme.cotes.forEach(cote => {
            themeNode.children.push({
              name: cote.cote,
              fullName: cote.description,
              description: cote.description,
              periode: cote.periode || theme.intitule,
              producer: cote.producteur,
              color: this.adjustColor(seriesData.color, 40)
            });
          });

          seriesNode.children.push(themeNode);
        });
      }

      hierarchy.children.push(seriesNode);
    });

    Object.keys(this.data.series_speciales).forEach(specialKey => {
      const special = this.data.series_speciales[specialKey];
      const specialNode = {
        name: specialKey.toUpperCase(),
        fullName: special.intitule,
        description: special.description,
        color: special.color || '#718096',
        children: []
      };

      if (special.instruments) {
        special.instruments.forEach(inst => {
          specialNode.children.push({
            name: inst.cote,
            fullName: inst.description,
            description: inst.description,
            periode: inst.periode,
            color: special.color
          });
        });
      }

      hierarchy.children.push(specialNode);
    });

    this.hierarchy = hierarchy;
  }

  adjustColor(hex, percent) {
    const num = parseInt(hex.replace('#', ''), 16);
    const amt = Math.round(2.55 * percent);
    const R = (num >> 16) + amt;
    const G = (num >> 8 & 0x00FF) + amt;
    const B = (num & 0x0000FF) + amt;
    return '#' + (
      0x1000000 +
      (R < 255 ? (R < 1 ? 0 : R) : 255) * 0x10000 +
      (G < 255 ? (G < 1 ? 0 : G) : 255) * 0x100 +
      (B < 255 ? (B < 1 ? 0 : B) : 255)
    ).toString(16).slice(1);
  }

  buildSeriesButtons() {
    const container = document.getElementById('seriesButtons');
    const series = this.data.cadre_classement.series;
    const specialKeys = Object.keys(this.data.series_speciales);

    container.innerHTML = '<button class="series-btn active" data-series="all">Toutes</button>';

    Object.keys(series).forEach(key => {
      const btn = document.createElement('button');
      btn.className = 'series-btn';
      btn.dataset.series = key;
      btn.textContent = key;
      btn.style.borderColor = series[key].color;
      btn.style.color = series[key].color;
      container.appendChild(btn);
    });
  }

  buildThemeList() {
    const container = document.getElementById('themeList');
    const themes = this.data.cadre_classement.series.W?.thematiques || {};

    container.innerHTML = '';

    Object.keys(themes).forEach(key => {
      const theme = themes[key];
      const item = document.createElement('div');
      item.className = 'theme-item';
      item.dataset.theme = key;
      item.innerHTML = `
        <span class="color-dot" style="background: ${this.data.cadre_classement.series.W.color}"></span>
        <span class="theme-name">${theme.intitule}</span>
      `;
      item.addEventListener('click', () => this.selectTheme(key));
      container.appendChild(item);
    });
  }

  selectTheme(themeKey) {
    const items = document.querySelectorAll('.theme-item');
    items.forEach(item => item.classList.remove('selected'));

    if (this.currentTheme === themeKey) {
      this.currentTheme = null;
    } else {
      this.currentTheme = themeKey;
      document.querySelector(`[data-theme="${themeKey}"]`)?.classList.add('selected');
    }

    this.updateResults();
  }

  collectAllInstruments() {
    this.allInstruments = [];

    const series = this.data.cadre_classement.series;
    Object.keys(series).forEach(key => {
      const s = series[key];
      if (s.instruments) {
        s.instruments.forEach(i => {
          this.allInstruments.push({ ...i, series: key, color: s.color });
        });
      }
      if (key === 'W' && s.thematiques) {
        Object.keys(s.thematiques).forEach(tk => {
          const t = s.thematiques[tk];
          t.cotes.forEach(c => {
            this.allInstruments.push({ ...c, series: key, theme: tk, themeName: t.intitule, color: s.color });
          });
        });
      }
    });

    Object.keys(this.data.series_speciales).forEach(key => {
      const s = this.data.series_speciales[key];
      if (s.instruments) {
        s.instruments.forEach(i => {
          this.allInstruments.push({ ...i, series: key.toUpperCase(), color: s.color });
        });
      }
    });
  }

  createSunburst() {
    const container = document.getElementById('sunburst');
    const width = 450;
    const height = 450;
    const radius = Math.min(width, height) / 2;

    d3.select(container).selectAll('*').remove();

    const svg = d3.select(container)
      .append('svg')
      .attr('viewBox', `0 0 ${width} ${height}`)
      .attr('width', '100%')
      .attr('height', '100%')
      .append('g')
      .attr('transform', `translate(${width / 2}, ${height / 2})`);

    const root = d3.hierarchy(this.hierarchy)
      .sum(d => d.children ? 0 : 1)
      .sort((a, b) => b.value - a.value);

    const partition = d3.partition()
      .size([2 * Math.PI, radius]);

    partition(root);

    const arc = d3.arc()
      .startAngle(d => d.x0)
      .endAngle(d => d.x1)
      .padAngle(0.01)
      .padRadius(radius / 2)
      .innerRadius(d => d.y0)
      .outerRadius(d => d.y1 - 1);

    const tooltip = document.getElementById('tooltip');

    const paths = svg.selectAll('path')
      .data(root.descendants().filter(d => d.depth))
      .join('path')
      .attr('d', arc)
      .attr('fill', d => d.data.color || '#718096')
      .attr('fill-opacity', d => d.depth === 1 ? 1 : 0.7)
      .attr('stroke', '#fff')
      .attr('stroke-width', 1)
      .style('cursor', 'pointer')
      .on('mouseover', (event, d) => {
        tooltip.innerHTML = `
          <strong>${d.data.name}</strong><br>
          ${d.data.fullName || d.data.description || ''}
        `;
        tooltip.classList.add('visible');
        d3.select(event.currentTarget).attr('fill-opacity', 1);
      })
      .on('mousemove', (event) => {
        tooltip.style.left = (event.clientX + 15) + 'px';
        tooltip.style.top = (event.clientY - 10) + 'px';
      })
      .on('mouseout', (event, d) => {
        tooltip.classList.remove('visible');
        d3.select(event.currentTarget).attr('fill-opacity', d.depth === 1 ? 1 : 0.7);
      })
      .on('click', (event, d) => {
        if (d.depth === 1) {
          this.showInstrument(d.data);
        } else if (d.depth === 2 && d.data.name !== d.parent.data.name) {
          this.filterBySeries(d.parent.data.name);
        }
      });

    svg.append('text')
      .attr('text-anchor', 'middle')
      .attr('dy', '-0.5em')
      .attr('class', 'sunburst-title')
      .style('font-size', '14px')
      .style('fill', '#718096')
      .text('Cliquer sur une série');

    this.sunburstPaths = paths;
    this.sunburstRoot = root;
  }

  filterBySeries(seriesKey) {
    this.currentFilter = seriesKey;
    
    document.querySelectorAll('.series-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.series === seriesKey);
    });

    this.updateResults();
    this.updateBreadcrumb(seriesKey);
  }

  showInstrument(instrument) {
    const panel = document.getElementById('detailsPanel');
    const content = document.getElementById('detailsContent');

    content.innerHTML = `
      <h3>${instrument.fullName || instrument.name}</h3>
      <div class="cote-display">${instrument.name}</div>
      
      <div class="section">
        <div class="section-title">Description</div>
        <div class="section-content">${instrument.description || 'Non renseignée'}</div>
      </div>
      
      ${instrument.periode ? `
      <div class="section">
        <div class="section-title">Période</div>
        <div class="section-content">${instrument.periode}</div>
      </div>
      ` : ''}
      
      ${instrument.producer ? `
      <div class="section">
        <div class="section-title">Producteur</div>
        <div class="section-content">${instrument.producer}</div>
      </div>
      ` : ''}
      
      <div class="section">
        <a href="https://www.archives13.fr/archive/recherche/fonds/n:93?Rech_cote=%22${encodeURIComponent(instrument.name)}%22" 
           target="_blank" 
           class="action-link">
          Rechercher dans le catalogue
        </a>
      </div>
    `;

    panel.classList.add('open');
  }

  setupEventListeners() {
    document.getElementById('closeDetails').addEventListener('click', () => {
      document.getElementById('detailsPanel').classList.remove('open');
    });

    document.querySelectorAll('.series-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        this.filterBySeries(btn.dataset.series);
      });
    });

    document.getElementById('searchInput').addEventListener('input', (e) => {
      this.searchTerm = e.target.value.toLowerCase();
      this.updateResults();
    });
  }

  updateResults() {
    let filtered = [...this.allInstruments];

    if (this.currentFilter !== 'all') {
      filtered = filtered.filter(i => i.series === this.currentFilter);
    }

    if (this.currentTheme) {
      filtered = filtered.filter(i => i.theme === this.currentTheme);
    }

    if (this.searchTerm) {
      filtered = filtered.filter(i => 
        (i.cote || i.name || '').toLowerCase().includes(this.searchTerm) ||
        (i.description || i.fullName || '').toLowerCase().includes(this.searchTerm)
      );
    }

    const container = document.getElementById('resultsList');
    const count = document.getElementById('resultsCount');

    count.textContent = `${filtered.length} instrument(s)`;

    container.innerHTML = filtered.slice(0, 50).map(item => `
      <div class="result-card" style="border-left-color: ${item.color || '#718096'}" data-instrument='${JSON.stringify(item).replace(/'/g, "&#39;")}'>
        <div class="cote">${item.cote || item.name}</div>
        <div class="title">${item.fullName || item.description || item.themeName || item.name}</div>
        <div class="description">${item.description || ''}</div>
        <div class="meta">
          ${item.series ? `<span>Série ${item.series}</span>` : ''}
          ${item.periode ? `<span>${item.periode}</span>` : ''}
        </div>
      </div>
    `).join('');

    container.querySelectorAll('.result-card').forEach(card => {
      card.addEventListener('click', () => {
        const data = JSON.parse(card.dataset.instrument.replace(/&#39;/g, "'"));
        this.showInstrument(data);
      });
    });
  }

  updateBreadcrumb(seriesKey) {
    const breadcrumb = document.getElementById('breadcrumb');
    const series = this.data.cadre_classement.series;
    const seriesData = series[seriesKey] || this.data.series_speciales[seriesKey.toLowerCase()];

    if (seriesData) {
      breadcrumb.innerHTML = `
        <span>AD13</span> /
        <span class="current">Série ${seriesKey} - ${seriesData.intitule}</span>
      `;
    }
  }
}

